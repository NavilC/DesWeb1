//funcion para realizar la multiplicacion de a y b
exports.multiplicacion = function(a,b){

    return parseFloat(a) * parseFloat(b);

};
// funcion para encontrar el doble de a
exports.doble = function( a, b ){

    return parseFloat(a) * parseFloat(b);

};
//funcion para encontra el triple de a
exports.triple = function(a,b){
    return parseFloat(a) * parseFloat(b);
}
//funcion para realizar la divicion de a entre b
exports.Division = function(a,b){
    return parseFloat(a) / parseFloat(b);
}